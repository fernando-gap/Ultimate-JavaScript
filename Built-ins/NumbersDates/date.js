// Built-ins

/**
 * Numbers & Dates
 */


Date();
/*

When called as a function, returns a string representation
of the current date and time, exactly as new
Date();.toString(); does.

*/


Date.now();
/*

Returns the numeric value corresponding to the current
time—the number of milliseconds elapsed since
January 1, 1970 00:00:00 UTC, with leap seconds ignored.

*/


Date.prototype.getDate();
/*

Returns the day of the month (1–31) for the specified
date according to local time.

*/


Date.prototype.getDay();
/*

Returns the day of the week (0–6) for the specified date
according to local time.

*/


Date.prototype.getFullYear();
/*

Returns the year (4 digits for 4-digit years) of the
specified date according to local time

*/


Date.prototype.getHours();
/*

Returns the hour (0–23) in the specified date according
to local time. Can contain options such as Milliseconds,
Minutes, month and time.

*/


Date.prototype.setDate();
/*

Sets the day of the month for a specified date according
to local time.

*/


Date.prototype.setFullYear();
/*

Sets the full year (e.g. 4 digits for 4-digit years) for
a specified date according to local time.

*/


Date.prototype.setHours();
/*

Sets the hours for a specified date according to local time.

*/


Date.prototype.setMilliseconds();
/*

Sets the milliseconds for a specified date according to local time.

*/


Date.prototype.setMinutes();
/*

Sets the minutes for a specified date according to local time.

*/

Date.prototype.setMonth();
/*

Sets the month for a specified date according to local time.

*/


Date.prototype.setSeconds();
/*

Sets the seconds for a specified date according to local time.

*/


Date.prototype.setTime();
